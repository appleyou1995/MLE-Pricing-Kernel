function plot_pit_delta_null_distribution( ...
    null_delta, observed_delta, face_color, ...
    plot_title, comparison_label, output_file, ...
    resolution, num_replications)

    null_delta = null_delta(isfinite(null_delta));

    if isempty(null_delta)
        error('No finite PIT pairwise Delta values are available.');
    end

    critical_value = prctile(null_delta, 95);
    p_value = (1 + sum(null_delta >= observed_delta)) / ...
        (1 + numel(null_delta));

    % A simple Gaussian kernel density curve is drawn over the histogram.
    % This avoids assuming that Delta follows a normal distribution.
    n = numel(null_delta);
    sample_std = std(null_delta);
    sample_iqr = prctile(null_delta, 75) - ...
        prctile(null_delta, 25);
    robust_scale = min(sample_std, sample_iqr / 1.34);

    if ~isfinite(robust_scale) || robust_scale <= 0
        robust_scale = max( ...
            (max(null_delta) - min(null_delta)) / 4, 1e-4);
    end

    bandwidth = 0.9 * robust_scale * n^(-1/5);

    if ~isfinite(bandwidth) || bandwidth <= 0
        bandwidth = 1e-4;
    end

    x_left = max(0, min([null_delta; observed_delta]) - 2 * bandwidth);
    x_right = max([null_delta; observed_delta; critical_value]) + ...
        2 * bandwidth;

    if x_right <= x_left
        x_right = x_left + 5 * bandwidth;
    end

    x_grid = linspace(x_left, x_right, 300);
    z = (x_grid(:) - null_delta(:)') ./ bandwidth;
    density = mean(exp(-0.5 .* z.^2), 2) ./ ...
        (sqrt(2 * pi) * bandwidth);

    fig = figure('Position', [80, 80, 780, 520], 'Color', 'w');

    histogram(null_delta, ...
        'NumBins', min(8, max(4, round(sqrt(n)))), ...
        'Normalization', 'pdf', ...
        'FaceColor', face_color, ...
        'FaceAlpha', 0.35, ...
        'EdgeColor', 'w', ...
        'DisplayName', 'Simulated Delta');
    hold on;

    plot(x_grid, density, ...
        'Color', face_color, ...
        'LineWidth', 2.5, ...
        'DisplayName', 'Smoothed null density');

    xline(observed_delta, '-', ...
        sprintf('Observed Delta = %.4f', observed_delta), ...
        'Color', [0.76, 0.25, 0.24], ...
        'LineWidth', 2, ...
        'LabelOrientation', 'horizontal', ...
        'HandleVisibility', 'off');

    xline(critical_value, '--', ...
        sprintf('95%% critical = %.4f', critical_value), ...
        'Color', [0.15, 0.20, 0.25], ...
        'LineWidth', 2, ...
        'LabelOrientation', 'horizontal', ...
        'HandleVisibility', 'off');

    hold off;
    grid on;
    xlabel('$\Delta=\max_j|\hat p_{A,j}-\hat p_{B,j}|$', ...
        'Interpreter', 'latex');
    ylabel('Estimated density under H_0', 'Interpreter', 'tex');
    title({sprintf('%s (N_{sim} = %d)', plot_title, num_replications), ...
        sprintf('%s; bootstrap p-value = %.3f', ...
        comparison_label, p_value)}, ...
        'Interpreter', 'tex');
    legend('Location', 'southeast', 'Box', 'off', ...
        'Interpreter', 'none');

    hide_axes_toolbars(fig);
    exportgraphics(fig, output_file, 'Resolution', resolution);
    close(fig);

    fprintf(['%s: observed Delta = %.6f, 95%% critical = %.6f, ', ...
        'bootstrap p-value = %.6f\n'], ...
        comparison_label, observed_delta, critical_value, p_value);
end


function hide_axes_toolbars(fig)

    axes_handles = findall(fig, 'Type', 'axes');

    for i = 1:numel(axes_handles)
        axtoolbar(axes_handles(i), {});
    end
end
