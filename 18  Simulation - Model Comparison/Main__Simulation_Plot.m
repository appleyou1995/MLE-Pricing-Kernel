%% Local plotting functions

function plot_null_distribution( ...
    statistics, observed_statistic, critical_value, ...
    face_color, plot_title, x_label, output_file, ...
    resolution, num_replications)

    statistics = statistics(isfinite(statistics));

    fig = figure('Position', [80, 80, 720, 500], 'Color', 'w');

    histogram(statistics, ...
        'NumBins', min(8, max(4, round(sqrt(numel(statistics))))), ...
        'Normalization', 'probability', ...
        'FaceColor', face_color, ...
        'FaceAlpha', 0.75, ...
        'EdgeColor', 'w');
    hold on;

    xline(observed_statistic, '-', ...
        sprintf('Observed = %.3f', observed_statistic), ...
        'Color', [0.76, 0.25, 0.24], ...
        'LineWidth', 2, ...
        'LabelOrientation', 'horizontal');

    xline(critical_value, '--', ...
        sprintf('95%% critical = %.3f', critical_value), ...
        'Color', [0.15, 0.20, 0.25], ...
        'LineWidth', 2, ...
        'LabelOrientation', 'horizontal');

    hold off;
    grid on;
    xlabel(x_label, 'Interpreter', 'latex');
    ylabel('Fraction of simulations');
    title(sprintf('%s (N_{sim} = %d)', plot_title, num_replications), ...
        'Interpreter', 'tex');

    hide_axes_toolbars(fig);
    exportgraphics(fig, output_file, 'Resolution', resolution);
    close(fig);
end


function plot_null_vs_alternative( ...
    null_statistics, alternative_statistics, ...
    observed_statistic, critical_value, ...
    null_color, alternative_color, ...
    plot_title, x_label, null_name, alternative_name, ...
    output_file, resolution, num_replications)

    null_statistics = null_statistics(isfinite(null_statistics));
    alternative_statistics = ...
        alternative_statistics(isfinite(alternative_statistics));

    all_statistics = [null_statistics; alternative_statistics];
    min_stat = min(all_statistics);
    max_stat = max(all_statistics);

    if min_stat == max_stat
        min_stat = min_stat - 0.5;
        max_stat = max_stat + 0.5;
    end

    num_bins = min(8, max(4, round(sqrt(numel(null_statistics)))));
    edges = linspace(min_stat, max_stat, num_bins + 1);

    fig = figure('Position', [80, 80, 760, 520], 'Color', 'w');

    histogram(null_statistics, edges, ...
        'Normalization', 'probability', ...
        'FaceColor', null_color, ...
        'FaceAlpha', 0.55, ...
        'EdgeColor', 'w', ...
        'DisplayName', null_name);
    hold on;

    histogram(alternative_statistics, edges, ...
        'Normalization', 'probability', ...
        'FaceColor', alternative_color, ...
        'FaceAlpha', 0.45, ...
        'EdgeColor', 'w', ...
        'DisplayName', alternative_name);

    xline(observed_statistic, '-', ...
        sprintf('Observed = %.3f', observed_statistic), ...
        'Color', [0.55, 0.10, 0.10], ...
        'LineWidth', 2, ...
        'HandleVisibility', 'off');

    xline(critical_value, '--', ...
        sprintf('95%% critical = %.3f', critical_value), ...
        'Color', [0.15, 0.20, 0.25], ...
        'LineWidth', 2, ...
        'HandleVisibility', 'off');

    hold off;
    grid on;
    xlabel(x_label, 'Interpreter', 'latex');
    ylabel('Fraction of simulations');
    title(sprintf('%s (N_{sim} = %d)', plot_title, num_replications), ...
        'Interpreter', 'tex');
    legend('Location', 'best', 'Box', 'off', ...
        'Interpreter', 'none');

    hide_axes_toolbars(fig);
    exportgraphics(fig, output_file, 'Resolution', resolution);
    close(fig);
end


function plot_pit_comparison( ...
    probability_A, probability_B, ...
    name_A, name_B, color_A, color_B, ...
    plot_title, output_file, resolution)

    probabilities = [probability_A(:), probability_B(:)];

    fig = figure('Position', [80, 80, 800, 500], 'Color', 'w');
    bars = bar(1:10, probabilities, 'grouped');
    bars(1).FaceColor = color_A;
    bars(2).FaceColor = color_B;
    bars(1).EdgeColor = 'none';
    bars(2).EdgeColor = 'none';
    hold on;
    yline(0.1, 'k--', 'Uniform target = 0.10', ...
        'LineWidth', 1.5, ...
        'LabelHorizontalAlignment', 'left');
    hold off;

    grid on;
    xlabel('PIT bin');
    ylabel('Probability');
    title(plot_title, 'Interpreter', 'none');
    legend({name_A, name_B}, ...
        'Location', 'best', 'Box', 'off', ...
        'Interpreter', 'none');
    xlim([0.4, 10.6]);

    upper_limit = max([0.13; probabilities(:) * 1.15]);
    ylim([0, upper_limit]);

    hide_axes_toolbars(fig);
    exportgraphics(fig, output_file, 'Resolution', resolution);
    close(fig);
end


