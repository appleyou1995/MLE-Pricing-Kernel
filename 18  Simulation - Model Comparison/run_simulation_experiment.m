%% Run one simulation experiment with two candidate models

function Results = run_simulation_experiment( ...
    Experiment, Data, num_simulations, num_bins_list, ...
    base_seed, batch_size, output_folder)

    num_bins_list = unique(num_bins_list(:)', 'stable');

    if isempty(num_bins_list) || ...
            any(~isfinite(num_bins_list)) || ...
            any(num_bins_list < 2) || ...
            any(num_bins_list ~= round(num_bins_list))
        error('num_bins_list must contain integers greater than one.');
    end

    num_bin_settings = numel(num_bins_list);
    Num_Bins_List = num_bins_list;

    Checkpoint_Settings = struct( ...
        'Experiment_Name', string(Experiment.Name), ...
        'Num_Simulations', num_simulations, ...
        'Num_Bins_List', Num_Bins_List, ...
        'Base_Seed', base_seed, ...
        'ModelA_Alpha', Experiment.ModelA.Alpha, ...
        'ModelA_Beta', Experiment.ModelA.Beta, ...
        'ModelA_Monotonicity', ...
            Experiment.ModelA.ImposeMonotonicity, ...
        'ModelB_Alpha', Experiment.ModelB.Alpha, ...
        'ModelB_Beta', Experiment.ModelB.Beta, ...
        'ModelB_Monotonicity', ...
            Experiment.ModelB.ImposeMonotonicity, ...
        'ComputePairwiseTest', ...
            Experiment.ComputePairwiseTest, ...
        'PITUniformityTarget', ...
            string(Experiment.PITUniformityTarget));

    if ~exist(output_folder, 'dir')
        mkdir(output_folder);
    end

    checkpoint_file = fullfile( ...
        output_folder, 'Simulation_Checkpoint.mat');
    raw_file = fullfile( ...
        output_folder, 'Simulation_Raw.mat');
    combined_summary_file = fullfile( ...
        output_folder, 'Simulation_Summary_AllBins.csv');

    empty_bin_probability_A = cell(1, num_bin_settings);
    empty_bin_probability_B = cell(1, num_bin_settings);

    for bin_idx = 1:num_bin_settings
        empty_bin_probability_A{bin_idx} = ...
            nan(1, num_bins_list(bin_idx));
        empty_bin_probability_B{bin_idx} = ...
            nan(1, num_bins_list(bin_idx));
    end

    empty_result = struct( ...
        'Simulation', NaN, ...
        'LL_A', NaN, ...
        'LL_B', NaN, ...
        'Beta_A', NaN, ...
        'Beta_B', NaN, ...
        'TestStatistic', NaN, ...
        'PIT_Sup_A', nan(1, num_bin_settings), ...
        'PIT_Sup_B', nan(1, num_bin_settings), ...
        'PIT_Gain_B', nan(1, num_bin_settings), ...
        'PIT_Pairwise_Delta', nan(1, num_bin_settings), ...
        'KS_A', NaN, ...
        'KS_B', NaN, ...
        'ExitFlag_A', NaN, ...
        'ExitFlag_B', NaN, ...
        'BinProbability_A', {empty_bin_probability_A}, ...
        'BinProbability_B', {empty_bin_probability_B});

    checkpoint_compatible = false;

    if exist(checkpoint_file, 'file')
        Saved = load(checkpoint_file);

        checkpoint_compatible = ...
            isfield(Saved, 'Results') && ...
            isfield(Saved, 'Completed') && ...
            isfield(Saved, 'Num_Bins_List') && ...
            isfield(Saved, 'Checkpoint_Settings') && ...
            numel(Saved.Results) == num_simulations && ...
            isequal( ...
                Saved.Num_Bins_List(:), ...
                Num_Bins_List(:)) && ...
            isequaln( ...
                Saved.Checkpoint_Settings, ...
                Checkpoint_Settings);

        if checkpoint_compatible
            Results = Saved.Results;
            Completed = Saved.Completed;

            fprintf('Resuming %s from replication %d.\n', ...
                Experiment.Name, Completed + 1);
        else
            warning([ ...
                'Ignoring an incompatible checkpoint in %s. ', ...
                'The new 10/20-bin run will start from replication 1.'], ...
                output_folder);
        end
    end

    if ~checkpoint_compatible
        Results = repmat(empty_result, num_simulations, 1);
        Completed = 0;
    end

    % Keep large common inputs on workers across all batches.
    Data_Const = [];
    DGP_Const = [];

    if Completed < num_simulations
        Data_Const = parallel.pool.Constant(Data);
        DGP_Const = parallel.pool.Constant(struct( ...
            'R_Axis', {Experiment.DGP_R_Axis}, ...
            'CDF', {Experiment.DGP_CDF}));
    else
        fprintf('%s is already complete; rebuilding output files.\n', ...
            Experiment.Name);
    end

    ModelA_Alpha = Experiment.ModelA.Alpha;
    ModelA_Beta = Experiment.ModelA.Beta;
    ModelA_ImposeMonotonicity = ...
        Experiment.ModelA.ImposeMonotonicity;

    ModelB_Alpha = Experiment.ModelB.Alpha;
    ModelB_Beta = Experiment.ModelB.Beta;
    ModelB_ImposeMonotonicity = ...
        Experiment.ModelB.ImposeMonotonicity;

    Need_Model_A = Experiment.ComputePairwiseTest || ...
        strcmpi(Experiment.PITUniformityTarget, 'A');
    Need_Model_B = Experiment.ComputePairwiseTest || ...
        strcmpi(Experiment.PITUniformityTarget, 'B');

    % Progress messages from parfor workers to the MATLAB client.
    Progress_Queue = parallel.pool.DataQueue;

    simulation_progress( ...
        'initialize', Completed, ...
        num_simulations, Experiment.Name);

    afterEach(Progress_Queue, ...
        @(simulation_id) simulation_progress( ...
            'update', simulation_id, ...
            num_simulations, Experiment.Name));

    for batch_start = (Completed + 1):batch_size:num_simulations
        batch_end = min( ...
            batch_start + batch_size - 1, num_simulations);
        simulation_indices = batch_start:batch_end;
        num_in_batch = numel(simulation_indices);

        Batch_Results = repmat( ...
            empty_result, num_in_batch, 1);

        fprintf('\n%s: simulations %d to %d of %d\n', ...
            Experiment.Name, ...
            batch_start, batch_end, num_simulations);

        parfor q = 1:num_in_batch
            simulation_id = simulation_indices(q);
            D = Data_Const.Value;
            G = DGP_Const.Value;

            % A separate deterministic seed for every replication.
            rng(base_seed + simulation_id, 'twister');

            R_sim = draw_returns_from_monthly_cdf( ...
                G.R_Axis, G.CDF);

            Simulated_Return = D.Realized_Return_Template;
            Simulated_Return.realized_ret = R_sim;

            LL_A = NaN;
            LL_B = NaN;
            exit_A = NaN;
            exit_B = NaN;
            Beta_A_hat = NaN;
            Beta_B_hat = NaN;
            pit_A = [];
            pit_B = [];

            if Need_Model_A
                [theta_A, LL_A, ~, exit_A, output_A] = ...
                    MLE_BSpline_estimation( ...
                    D.Smooth_AllR, D.Smooth_AllR_RND, ...
                    Simulated_Return, D.Risk_Free_Rate, ...
                    D.b, ModelA_Alpha, ModelA_Beta, ...
                    D.Global_Min_R, D.Global_Max_R, ...
                    ModelA_ImposeMonotonicity, ...
                    D.Basis_Precomputed);

                Beta_A_hat = output_A.SelectedBeta;

                [~, ~, ~, ~, pit_A] = ...
                    log_likelihood_bspline( ...
                    theta_A, R_sim, D.Risk_Free_Rate, ...
                    D.Basis_Precomputed, ...
                    D.Smooth_AllR, D.Smooth_AllR_RND, ...
                    D.months, ModelA_Alpha, Beta_A_hat);
            end

            if Need_Model_B
                [theta_B, LL_B, ~, exit_B, output_B] = ...
                    MLE_BSpline_estimation( ...
                    D.Smooth_AllR, D.Smooth_AllR_RND, ...
                    Simulated_Return, D.Risk_Free_Rate, ...
                    D.b, ModelB_Alpha, ModelB_Beta, ...
                    D.Global_Min_R, D.Global_Max_R, ...
                    ModelB_ImposeMonotonicity, ...
                    D.Basis_Precomputed);

                Beta_B_hat = output_B.SelectedBeta;

                [~, ~, ~, ~, pit_B] = ...
                    log_likelihood_bspline( ...
                    theta_B, R_sim, D.Risk_Free_Rate, ...
                    D.Basis_Precomputed, ...
                    D.Smooth_AllR, D.Smooth_AllR_RND, ...
                    D.months, ModelB_Alpha, Beta_B_hat);
            end

            One = empty_result;
            One.Simulation = simulation_id;
            One.LL_A = LL_A;
            One.LL_B = LL_B;
            One.Beta_A = Beta_A_hat;
            One.Beta_B = Beta_B_hat;
            if Experiment.ComputePairwiseTest
                One.TestStatistic = 2 * (LL_B - LL_A);
            end
            One.ExitFlag_A = exit_A;
            One.ExitFlag_B = exit_B;

            for bin_idx = 1:num_bin_settings
                num_bins_now = num_bins_list(bin_idx);
                Stats_A = [];
                Stats_B = [];

                if Need_Model_A
                    Stats_A = compute_pit_statistics( ...
                        pit_A, num_bins_now);
                    One.PIT_Sup_A(bin_idx) = ...
                        Stats_A.SupUniform;
                    One.BinProbability_A{bin_idx} = ...
                        Stats_A.BinProbability;

                    if bin_idx == 1
                        One.KS_A = Stats_A.KS;
                    end
                end

                if Need_Model_B
                    Stats_B = compute_pit_statistics( ...
                        pit_B, num_bins_now);
                    One.PIT_Sup_B(bin_idx) = ...
                        Stats_B.SupUniform;
                    One.BinProbability_B{bin_idx} = ...
                        Stats_B.BinProbability;

                    if bin_idx == 1
                        One.KS_B = Stats_B.KS;
                    end
                end

                if Experiment.ComputePairwiseTest
                    One.PIT_Gain_B(bin_idx) = ...
                        Stats_A.SupUniform - Stats_B.SupUniform;
                    One.PIT_Pairwise_Delta(bin_idx) = max(abs( ...
                        Stats_A.BinProbability - ...
                        Stats_B.BinProbability));
                end
            end

            Batch_Results(q) = One;
            send(Progress_Queue, simulation_id);
        end

        Results(simulation_indices) = Batch_Results;
        Completed = batch_end;

        save(checkpoint_file, ...
            'Results', 'Completed', ...
            'Num_Bins_List', 'Checkpoint_Settings', '-v7.3');
    end

    if ~isempty(Data_Const)
        delete(Data_Const);
    end

    if ~isempty(DGP_Const)
        delete(DGP_Const);
    end

    save(raw_file, ...
        'Results', 'Experiment', 'Num_Bins_List', '-v7.3');

    LL_A_all = [Results.LL_A]';
    LL_B_all = [Results.LL_B]';
    Beta_A_all = [Results.Beta_A]';
    Beta_B_all = [Results.Beta_B]';
    lr_statistics = [Results.TestStatistic]';
    KS_A_all = [Results.KS_A]';
    KS_B_all = [Results.KS_B]';
    Exit_A_all = [Results.ExitFlag_A]';
    Exit_B_all = [Results.ExitFlag_B]';

    PIT_Sup_A_all = vertcat(Results.PIT_Sup_A);
    PIT_Sup_B_all = vertcat(Results.PIT_Sup_B);
    PIT_Gain_B_all = vertcat(Results.PIT_Gain_B);
    Delta_all = vertcat(Results.PIT_Pairwise_Delta);

    All_Summaries = table();

    for bin_idx = 1:num_bin_settings
        num_bins_now = num_bins_list(bin_idx);

        csv_file = fullfile(output_folder, sprintf( ...
            'Simulation_Scalars_Bins%d.csv', num_bins_now));
        summary_file = fullfile(output_folder, sprintf( ...
            'Simulation_Summary_Bins%d.csv', num_bins_now));
        bins_file = fullfile(output_folder, sprintf( ...
            'Mean_PIT_Bins%d.csv', num_bins_now));

        scalar_table = table( ...
            [Results.Simulation]', ...
            LL_A_all, LL_B_all, ...
            Beta_A_all, Beta_B_all, ...
            lr_statistics, ...
            PIT_Sup_A_all(:, bin_idx), ...
            PIT_Sup_B_all(:, bin_idx), ...
            PIT_Gain_B_all(:, bin_idx), ...
            Delta_all(:, bin_idx), ...
            KS_A_all, KS_B_all, ...
            Exit_A_all, Exit_B_all, ...
            'VariableNames', { ...
                'Simulation', ...
                'LL_A', 'LL_B', ...
                'Beta_A', 'Beta_B', ...
                'TestStatistic', ...
                'PIT_Sup_A', 'PIT_Sup_B', ...
                'PIT_Gain_B', ...
                'PIT_Pairwise_Delta', ...
                'KS_A', 'KS_B', ...
                'ExitFlag_A', 'ExitFlag_B'});

        writetable(scalar_table, csv_file);

        if strcmpi(Experiment.PITUniformityTarget, 'A')
            PIT_Target_Model = string(Experiment.ModelA.Name);
            observed_pit_sup = ...
                Experiment.ObservedPITSup_A(bin_idx);
            successful = ...
                isfinite(PIT_Sup_A_all(:, bin_idx)) & ...
                Exit_A_all > 0;
            simulated_pit_sup_all = ...
                PIT_Sup_A_all(:, bin_idx);
        elseif strcmpi(Experiment.PITUniformityTarget, 'B')
            PIT_Target_Model = string(Experiment.ModelB.Name);
            observed_pit_sup = ...
                Experiment.ObservedPITSup_B(bin_idx);
            successful = ...
                isfinite(PIT_Sup_B_all(:, bin_idx)) & ...
                Exit_B_all > 0;
            simulated_pit_sup_all = ...
                PIT_Sup_B_all(:, bin_idx);
        else
            error('PITUniformityTarget must be A or B.');
        end

        if ~any(successful)
            warning([ ...
                'No successful replications for %s with %d bins.'], ...
                Experiment.Name, num_bins_now);
            continue
        end

        num_valid = sum(successful);
        simulated_pit_sup = ...
            simulated_pit_sup_all(successful);

        pit_tail_count = sum( ...
            simulated_pit_sup >= observed_pit_sup);
        pit_pvalue_simulated = ...
            pit_tail_count / num_valid;
        pit_pvalue_plus_one = ...
            (1 + pit_tail_count) / (1 + num_valid);

        if Experiment.ComputePairwiseTest
            pairwise_successful = ...
                isfinite(lr_statistics) & ...
                isfinite(Delta_all(:, bin_idx)) & ...
                Exit_A_all > 0 & Exit_B_all > 0;

            valid_lr = lr_statistics(pairwise_successful);
            valid_delta = ...
                Delta_all(pairwise_successful, bin_idx);
            num_pairwise_valid = sum(pairwise_successful);

            if num_pairwise_valid > 0
                lr_tail_count = sum( ...
                    valid_lr >= ...
                    Experiment.ObservedTestStatistic);
                lr_pvalue_simulated = ...
                    lr_tail_count / num_pairwise_valid;
                lr_pvalue_plus_one = ...
                    (1 + lr_tail_count) / ...
                    (1 + num_pairwise_valid);

                delta_tail_count = sum( ...
                    valid_delta >= ...
                    Experiment.ObservedPITDelta(bin_idx));
                delta_pvalue_simulated = ...
                    delta_tail_count / num_pairwise_valid;
                delta_pvalue_plus_one = ...
                    (1 + delta_tail_count) / ...
                    (1 + num_pairwise_valid);

                mean_lr = mean(valid_lr);
                median_lr = prctile(valid_lr, 50);
                p95_lr = prctile(valid_lr, 95);
                mean_delta = mean(valid_delta);
                p95_delta = prctile(valid_delta, 95);
                fraction_ll_b_greater = mean( ...
                    LL_B_all(pairwise_successful) > ...
                    LL_A_all(pairwise_successful));
                fraction_pit_b_closer = mean( ...
                    PIT_Gain_B_all( ...
                    pairwise_successful, bin_idx) > 0);
            else
                warning([ ...
                    'No successful pairwise replications for ', ...
                    '%s with %d bins.'], ...
                    Experiment.Name, num_bins_now);
                lr_pvalue_simulated = NaN;
                lr_pvalue_plus_one = NaN;
                delta_pvalue_simulated = NaN;
                delta_pvalue_plus_one = NaN;
                mean_lr = NaN;
                median_lr = NaN;
                p95_lr = NaN;
                mean_delta = NaN;
                p95_delta = NaN;
                fraction_ll_b_greater = NaN;
                fraction_pit_b_closer = NaN;
            end
        else
            lr_pvalue_simulated = NaN;
            lr_pvalue_plus_one = NaN;
            delta_pvalue_simulated = NaN;
            delta_pvalue_plus_one = NaN;
            mean_lr = NaN;
            median_lr = NaN;
            p95_lr = NaN;
            mean_delta = NaN;
            p95_delta = NaN;
            fraction_ll_b_greater = NaN;
            fraction_pit_b_closer = NaN;
        end

        summary_table = table( ...
            string(Experiment.Name), ...
            num_bins_now, ...
            string(Experiment.ModelA.Name), ...
            string(Experiment.ModelB.Name), ...
            Experiment.ComputePairwiseTest, ...
            Experiment.ObservedTestStatistic, ...
            lr_pvalue_simulated, ...
            lr_pvalue_plus_one, ...
            Experiment.ObservedPITDelta(bin_idx), ...
            delta_pvalue_simulated, ...
            delta_pvalue_plus_one, ...
            PIT_Target_Model, ...
            observed_pit_sup, ...
            pit_pvalue_simulated, ...
            pit_pvalue_plus_one, ...
            num_valid, ...
            mean_lr, ...
            median_lr, ...
            p95_lr, ...
            mean_delta, ...
            p95_delta, ...
            mean(simulated_pit_sup), ...
            prctile(simulated_pit_sup, 95), ...
            fraction_ll_b_greater, ...
            fraction_pit_b_closer, ...
            'VariableNames', { ...
                'Experiment', ...
                'Num_Bins', ...
                'Model_A', ...
                'Model_B', ...
                'Is_Pairwise_Null_DGP', ...
                'Observed_LR', ...
                'Simulated_LR_PValue', ...
                'PlusOne_LR_PValue', ...
                'Observed_PIT_Pairwise_Delta', ...
                'Simulated_PIT_Delta_PValue', ...
                'PlusOne_PIT_Delta_PValue', ...
                'PIT_Uniformity_Target_Model', ...
                'Observed_PIT_SupUniform', ...
                'Simulated_PIT_Uniformity_PValue', ...
                'PlusOne_PIT_Uniformity_PValue', ...
                'Successful_Replications', ...
                'Mean_LR', ...
                'Median_LR', ...
                'P95_LR', ...
                'Mean_PIT_Pairwise_Delta', ...
                'P95_PIT_Pairwise_Delta', ...
                'Mean_PIT_SupUniform', ...
                'P95_PIT_SupUniform', ...
                'Fraction_LL_B_Greater', ...
                'Fraction_PIT_B_CloserToUniform'});

        writetable(summary_table, summary_file);

        if isempty(All_Summaries)
            All_Summaries = summary_table;
        else
            All_Summaries = [ ...
                All_Summaries; summary_table]; %#ok<AGROW>
        end

        bin_A_matrix = nan(num_simulations, num_bins_now);
        bin_B_matrix = nan(num_simulations, num_bins_now);

        for simulation_idx = 1:num_simulations
            bin_A_matrix(simulation_idx, :) = ...
                Results(simulation_idx).BinProbability_A{bin_idx};
            bin_B_matrix(simulation_idx, :) = ...
                Results(simulation_idx).BinProbability_B{bin_idx};
        end

        mean_bin_A = mean( ...
            bin_A_matrix(successful, :), 1, 'omitnan')';
        mean_bin_B = mean( ...
            bin_B_matrix(successful, :), 1, 'omitnan')';

        bin_table = table( ...
            (1:num_bins_now)', ...
            ((0:num_bins_now-1)' ./ num_bins_now), ...
            ((1:num_bins_now)' ./ num_bins_now), ...
            mean_bin_A, mean_bin_B, ...
            'VariableNames', { ...
                'Bin', 'LowerEdge', 'UpperEdge', ...
                'MeanProbability_A', ...
                'MeanProbability_B'});

        writetable(bin_table, bins_file);
    end

    if ~isempty(All_Summaries)
        writetable(All_Summaries, combined_summary_file);
    end
end


function R_sim = draw_returns_from_monthly_cdf( ...
    R_axis_cell, F_cell)

    T = numel(R_axis_cell);
    R_sim = zeros(T, 1);

    for t = 1:T
        R_axis_t = R_axis_cell{t}(:);
        F_t = F_cell{t}(:);

        [F_unique, idx_unique] = unique(F_t, 'stable');
        R_unique = R_axis_t(idx_unique);

        if numel(F_unique) < 2
            error([ ...
                'Physical CDF at t=%d has fewer than ', ...
                'two unique values.'], t);
        end

        u = rand;
        R_sim(t) = interp1( ...
            F_unique, R_unique, u, 'linear', 'extrap');
    end
end


function simulation_progress( ...
    action, value, total, experiment_name)

    persistent Completed_Count
    persistent Starting_Count
    persistent Total_Count
    persistent Progress_Timer
    persistent Experiment_Name

    switch action
        case 'initialize'
            Completed_Count = value;
            Starting_Count = value;
            Total_Count = total;
            Progress_Timer = tic;
            Experiment_Name = char(experiment_name);

            fprintf('\n==================================================\n');
            fprintf('Starting experiment: %s\n', ...
                Experiment_Name);
            fprintf('Previously completed: %d/%d\n', ...
                Completed_Count, Total_Count);
            fprintf('==================================================\n');

        case 'update'
            Completed_Count = Completed_Count + 1;

            Newly_Completed = ...
                Completed_Count - Starting_Count;
            Elapsed_Seconds = toc(Progress_Timer);
            Average_Seconds = ...
                Elapsed_Seconds / max(Newly_Completed, 1);
            Remaining_Seconds = ...
                (Total_Count - Completed_Count) * ...
                Average_Seconds;

            fprintf([ ...
                '[%s] Completed %d/%d (%.1f%%) | ', ...
                'elapsed %.1f min | ETA %.1f min\n'], ...
                Experiment_Name, ...
                Completed_Count, Total_Count, ...
                100 * Completed_Count / Total_Count, ...
                Elapsed_Seconds / 60, ...
                Remaining_Seconds / 60);
    end
end
