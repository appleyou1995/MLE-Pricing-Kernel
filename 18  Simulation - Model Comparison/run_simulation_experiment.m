%% Run one simulation experiment with two candidate models

function Results = run_simulation_experiment( ...
    Experiment, Data, num_simulations, num_bins, ...
    base_seed, batch_size, output_folder)

    if ~exist(output_folder, 'dir')
        mkdir(output_folder);
    end

    checkpoint_file = fullfile(output_folder, 'Simulation_Checkpoint.mat');
    raw_file        = fullfile(output_folder, 'Simulation_Raw.mat');
    csv_file        = fullfile(output_folder, 'Simulation_Scalars.csv');
    summary_file    = fullfile(output_folder, 'Simulation_Summary.csv');
    bins_file       = fullfile(output_folder, 'Mean_PIT_Bins.csv');

    empty_result = struct( ...
        'Simulation', NaN, ...
        'LL_A', NaN, ...
        'LL_B', NaN, ...
        'TestStatistic', NaN, ...
        'PIT_Sup_A', NaN, ...
        'PIT_Sup_B', NaN, ...
        'PIT_Gain_B', NaN, ...
        'PIT_Pairwise_Delta', NaN, ...
        'KS_A', NaN, ...
        'KS_B', NaN, ...
        'ExitFlag_A', NaN, ...
        'ExitFlag_B', NaN, ...
        'BinProbability_A', nan(1, num_bins), ...
        'BinProbability_B', nan(1, num_bins));

    if exist(checkpoint_file, 'file')
        Saved = load(checkpoint_file, 'Results', 'Completed');
        Results   = Saved.Results;
        Completed = Saved.Completed;

        if numel(Results) ~= num_simulations
            error(['The checkpoint was created with a different number ', ...
                'of simulations. Delete the checkpoint or restore the ', ...
                'original num_simulations setting.']);
        end

        fprintf('Resuming %s from replication %d.\n', ...
            Experiment.Name, Completed + 1);
    else
        Results = repmat(empty_result, num_simulations, 1);
        Completed = 0;
    end

    % Keep large common inputs on workers across all batches.
    Data_Const = [];
    DGP_Const  = [];

    if Completed < num_simulations
        Data_Const = parallel.pool.Constant(Data);
        DGP_Const = parallel.pool.Constant(struct( ...
            'R_Axis', {Experiment.DGP_R_Axis}, ...
            'CDF', {Experiment.DGP_CDF}));
    else
        fprintf('%s is already complete; rebuilding summary files.\n', ...
            Experiment.Name);
    end

    % Extract scalar experiment settings once before parfor.
    ModelA_Alpha = Experiment.ModelA.Alpha;
    ModelA_Beta  = Experiment.ModelA.Beta;
    ModelA_ImposeMonotonicity = ...
        Experiment.ModelA.ImposeMonotonicity;

    ModelB_Alpha = Experiment.ModelB.Alpha;
    ModelB_Beta  = Experiment.ModelB.Beta;
    ModelB_ImposeMonotonicity = ...
        Experiment.ModelB.ImposeMonotonicity;

    for batch_start = (Completed + 1):batch_size:num_simulations
        batch_end = min(batch_start + batch_size - 1, num_simulations);
        simulation_indices = batch_start:batch_end;
        num_in_batch = numel(simulation_indices);

        Batch_Results = repmat(empty_result, num_in_batch, 1);

        fprintf('\n%s: simulations %d to %d of %d\n', ...
            Experiment.Name, batch_start, batch_end, num_simulations);

        parfor q = 1:num_in_batch
            simulation_id = simulation_indices(q);
            D = Data_Const.Value;
            G = DGP_Const.Value;

            % A separate deterministic seed for every replication.
            rng(base_seed + simulation_id, 'twister');

            R_sim = draw_returns_from_monthly_cdf( ...
                G.R_Axis, G.CDF);

            Simulated_Return = D.Realized_Return_Template;
            Simulated_Return.realized_ret = R_sim;

            [theta_A, LL_A, ~, exit_A] = MLE_BSpline_estimation( ...
                D.Smooth_AllR, D.Smooth_AllR_RND, ...
                Simulated_Return, D.Risk_Free_Rate, ...
                D.b, ...
                ModelA_Alpha, ...
                ModelA_Beta, ...
                D.Global_Min_R, D.Global_Max_R, ...
                ModelA_ImposeMonotonicity, ...
                D.Basis_Precomputed);

            [theta_B, LL_B, ~, exit_B] = MLE_BSpline_estimation( ...
                D.Smooth_AllR, D.Smooth_AllR_RND, ...
                Simulated_Return, D.Risk_Free_Rate, ...
                D.b, ...
                ModelB_Alpha, ...
                ModelB_Beta, ...
                D.Global_Min_R, D.Global_Max_R, ...
                ModelB_ImposeMonotonicity, ...
                D.Basis_Precomputed);

            [~, ~, ~, ~, pit_A] = log_likelihood_bspline( ...
                theta_A, R_sim, D.Risk_Free_Rate, ...
                D.Basis_Precomputed, ...
                D.Smooth_AllR, D.Smooth_AllR_RND, D.months, ...
                ModelA_Alpha, ModelA_Beta);

            [~, ~, ~, ~, pit_B] = log_likelihood_bspline( ...
                theta_B, R_sim, D.Risk_Free_Rate, ...
                D.Basis_Precomputed, ...
                D.Smooth_AllR, D.Smooth_AllR_RND, D.months, ...
                ModelB_Alpha, ModelB_Beta);

            Stats_A = compute_pit_statistics(pit_A, num_bins);
            Stats_B = compute_pit_statistics(pit_B, num_bins);

            One = empty_result;
            One.Simulation = simulation_id;
            One.LL_A = LL_A;
            One.LL_B = LL_B;
            One.TestStatistic = 2 * (LL_B - LL_A);
            One.PIT_Sup_A = Stats_A.SupUniform;
            One.PIT_Sup_B = Stats_B.SupUniform;
            One.PIT_Gain_B = Stats_A.SupUniform - Stats_B.SupUniform;
            One.PIT_Pairwise_Delta = max(abs( ...
                Stats_A.BinProbability - Stats_B.BinProbability));
            One.KS_A = Stats_A.KS;
            One.KS_B = Stats_B.KS;
            One.ExitFlag_A = exit_A;
            One.ExitFlag_B = exit_B;
            One.BinProbability_A = Stats_A.BinProbability;
            One.BinProbability_B = Stats_B.BinProbability;

            Batch_Results(q) = One;
        end

        Results(simulation_indices) = Batch_Results;
        Completed = batch_end;

        save(checkpoint_file, 'Results', 'Completed', '-v7.3');
    end

    if ~isempty(Data_Const)
        delete(Data_Const);
    end

    if ~isempty(DGP_Const)
        delete(DGP_Const);
    end

    save(raw_file, 'Results', 'Experiment', '-v7.3');

    scalar_table = table( ...
        [Results.Simulation]', ...
        [Results.LL_A]', ...
        [Results.LL_B]', ...
        [Results.TestStatistic]', ...
        [Results.PIT_Sup_A]', ...
        [Results.PIT_Sup_B]', ...
        [Results.PIT_Gain_B]', ...
        [Results.PIT_Pairwise_Delta]', ...
        [Results.KS_A]', ...
        [Results.KS_B]', ...
        [Results.ExitFlag_A]', ...
        [Results.ExitFlag_B]', ...
        'VariableNames', { ...
            'Simulation', ...
            'LL_A', 'LL_B', 'TestStatistic', ...
            'PIT_Sup_A', 'PIT_Sup_B', 'PIT_Gain_B', ...
            'PIT_Pairwise_Delta', ...
            'KS_A', 'KS_B', ...
            'ExitFlag_A', 'ExitFlag_B'});

    writetable(scalar_table, csv_file);

    test_statistics = [Results.TestStatistic]';
    successful = isfinite(test_statistics) & ...
        [Results.ExitFlag_A]' > 0 & [Results.ExitFlag_B]' > 0;

    valid_statistics = test_statistics(successful);

    if isempty(valid_statistics)
        warning('No successful simulation replications for %s.', ...
            Experiment.Name);
        return
    end

    bootstrap_pvalue = ...
        (1 + sum(valid_statistics >= Experiment.ObservedTestStatistic)) / ...
        (1 + numel(valid_statistics));

    LL_A_all = [Results.LL_A]';
    LL_B_all = [Results.LL_B]';
    PIT_Gain_B_all = [Results.PIT_Gain_B]';

    summary_table = table( ...
        string(Experiment.Name), ...
        string(Experiment.ModelA.Name), ...
        string(Experiment.ModelB.Name), ...
        Experiment.ObservedTestStatistic, ...
        bootstrap_pvalue, ...
        numel(valid_statistics), ...
        mean(valid_statistics), ...
        prctile(valid_statistics, 50), ...
        prctile(valid_statistics, 90), ...
        prctile(valid_statistics, 95), ...
        mean(LL_B_all(successful) > LL_A_all(successful)), ...
        mean(PIT_Gain_B_all(successful) > 0), ...
        'VariableNames', { ...
            'Experiment', 'Model_A', 'Model_B', ...
            'Observed_TestStatistic', 'Bootstrap_UpperTail_PValue', ...
            'Successful_Replications', ...
            'Mean_TestStatistic', 'Median_TestStatistic', ...
            'P90_TestStatistic', 'P95_TestStatistic', ...
            'Fraction_LL_B_Greater', ...
            'Fraction_PIT_B_CloserToUniform'});

    writetable(summary_table, summary_file);

    bin_A = vertcat(Results.BinProbability_A);
    bin_B = vertcat(Results.BinProbability_B);
    mean_bin_A = mean(bin_A(successful, :), 1, 'omitnan')';
    mean_bin_B = mean(bin_B(successful, :), 1, 'omitnan')';

    bin_table = table( ...
        (1:num_bins)', ...
        ((0:num_bins-1)' ./ num_bins), ...
        ((1:num_bins)' ./ num_bins), ...
        mean_bin_A, mean_bin_B, ...
        'VariableNames', { ...
            'Bin', 'LowerEdge', 'UpperEdge', ...
            'MeanProbability_A', 'MeanProbability_B'});

    writetable(bin_table, bins_file);
end


function R_sim = draw_returns_from_monthly_cdf(R_axis_cell, F_cell)

    T = numel(R_axis_cell);
    R_sim = zeros(T, 1);

    for t = 1:T
        R_axis_t = R_axis_cell{t}(:);
        F_t      = F_cell{t}(:);

        [F_unique, idx_unique] = unique(F_t, 'stable');
        R_unique = R_axis_t(idx_unique);

        if numel(F_unique) < 2
            error('Physical CDF at t=%d has fewer than two unique values.', t);
        end

        u = rand;
        R_sim(t) = interp1( ...
            F_unique, R_unique, u, 'linear', 'extrap');
    end
end
