num_bin_settings = numel(Num_Bins_List);

Observed_Delta_Shape = nan(1, num_bin_settings);
Observed_Delta_Distortion = nan(1, num_bin_settings);
Observed_Sup_C = nan(1, num_bin_settings);
Observed_Sup_U = nan(1, num_bin_settings);
Observed_Sup_N = nan(1, num_bin_settings);
Observed_Sup_D = nan(1, num_bin_settings);

for bin_idx = 1:num_bin_settings
    num_bins_now = Num_Bins_List(bin_idx);
    field_name = sprintf('Bins%d', num_bins_now);

    Observed_PIT.(field_name).Constraint = ...
        compute_pit_statistics(pit_C, num_bins_now);
    Observed_PIT.(field_name).Unconstraint = ...
        compute_pit_statistics(pit_U, num_bins_now);
    Observed_PIT.(field_name).Undistorted = ...
        compute_pit_statistics(pit_N, num_bins_now);
    Observed_PIT.(field_name).Distorted = ...
        compute_pit_statistics(pit_D, num_bins_now);

    Observed_Delta_Shape(bin_idx) = max(abs( ...
        Observed_PIT.(field_name).Constraint.BinProbability - ...
        Observed_PIT.(field_name).Unconstraint.BinProbability));

    Observed_Delta_Distortion(bin_idx) = max(abs( ...
        Observed_PIT.(field_name).Undistorted.BinProbability - ...
        Observed_PIT.(field_name).Distorted.BinProbability));

    Observed_Sup_C(bin_idx) = ...
        Observed_PIT.(field_name).Constraint.SupUniform;
    Observed_Sup_U(bin_idx) = ...
        Observed_PIT.(field_name).Unconstraint.SupUniform;
    Observed_Sup_N(bin_idx) = ...
        Observed_PIT.(field_name).Undistorted.SupUniform;
    Observed_Sup_D(bin_idx) = ...
        Observed_PIT.(field_name).Distorted.SupUniform;
end

