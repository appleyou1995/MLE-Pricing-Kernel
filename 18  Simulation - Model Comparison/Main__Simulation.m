%% Simulation settings

Target_TTM = 30;
b = 6;

Num_Simulations = 50;
Num_Bins_List   = [10, 20];
Batch_Size      = 10;
Base_Seed       = 19950313;
Reuse_Observed_Fits = true;

% Shape comparison:
