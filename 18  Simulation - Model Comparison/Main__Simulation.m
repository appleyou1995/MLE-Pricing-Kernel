%% Load realized returns

FileName = ['Realized_Return_TTM_', num2str(Target_TTM), '.csv'];
Realized_Return = readtable(fullfile(Path_Data, FileName));


%% Load risk-free gross factors

FileName = 'Risk_Free_GrossFactor_ByTargetTTM.csv';
Risk_Free_Rate_All = readtable(fullfile(Path_Data, FileName));


%% Load Q-measure PDF tables

Smooth_AllR = [];
Smooth_AllR_RND = [];

years_to_merge = 1996:2025;

for year = years_to_merge
    input_filename = fullfile(Path_RND, ...
        sprintf('TTM_%d_RND_Tables_%d.mat', Target_TTM, year));

    if exist(input_filename, 'file')
        Loaded = load(input_filename);
        Smooth_AllR = [Smooth_AllR, ...
            Loaded.Table_Smooth_AllR]; %#ok<AGROW>
        Smooth_AllR_RND = [Smooth_AllR_RND, ...
            Loaded.Table_Smooth_AllR_RND]; %#ok<AGROW>
    else
        warning('File %s does not exist.', input_filename);
    end
end

clear Loaded FileName input_filename year


%% Align RND, realized returns, and risk-free rates

rnd_var_names = string(Smooth_AllR_RND.Properties.VariableNames);
rnd_dates = str2double(regexp( ...
    rnd_var_names, '\d+', 'match', 'once'));

realized_dates = double(Realized_Return.date);

rf_date_col = ['date_', num2str(Target_TTM)];
rf_rate_col = ['rf_gross_TTM', num2str(Target_TTM)];

rf_dates_all  = double(Risk_Free_Rate_All.(rf_date_col));
rf_values_all = double(Risk_Free_Rate_All.(rf_rate_col));

idx_rf_valid = isfinite(rf_dates_all) & isfinite(rf_values_all);
rf_dates_all  = rf_dates_all(idx_rf_valid);
rf_values_all = rf_values_all(idx_rf_valid);

idx_master = ...
    ismember(realized_dates, rnd_dates) & ...
    ismember(realized_dates, rf_dates_all);

common_dates = sort(realized_dates(idx_master));

if isempty(common_dates)
    error('No common dates across the three input datasets.');
end

[tf_rnd, idx_rnd] = ismember(common_dates, rnd_dates);
[tf_ret, idx_ret] = ismember(common_dates, realized_dates);
[tf_rf,  idx_rf]  = ismember(common_dates, rf_dates_all);

if ~all(tf_rnd) || ~all(tf_ret) || ~all(tf_rf)
    error('Date alignment failed.');
end

Smooth_AllR     = Smooth_AllR(:, idx_rnd);
Smooth_AllR_RND = Smooth_AllR_RND(:, idx_rnd);
Realized_Return = Realized_Return(idx_ret, :);
Risk_Free_Rate  = rf_values_all(idx_rf);

R_observed = Realized_Return.realized_ret(:);
Risk_Free_Rate = Risk_Free_Rate(:);

fprintf('\nAligned sample: %d months, %08.0f to %08.0f.\n', ...
    numel(common_dates), common_dates(1), common_dates(end));

clear idx_master idx_rf_valid idx_rnd idx_ret idx_rf
clear tf_rnd tf_ret tf_rf rnd_dates realized_dates
clear rf_dates_all rf_values_all rf_date_col rf_rate_col
clear Risk_Free_Rate_All rnd_var_names


%% Global return range and precomputed B-spline basis

Global_Min_R = Inf;
Global_Max_R = -Inf;
months = Smooth_AllR.Properties.VariableNames;

for t = 1:numel(months)
    R_axis_t = Smooth_AllR.(months{t});
    Global_Min_R = min(Global_Min_R, min(R_axis_t));
    Global_Max_R = max(Global_Max_R, max(R_axis_t));
end

Global_Min_R = Global_Min_R * 0.9;
Global_Max_R = Global_Max_R * 1.1;

n_degree = 5;
k_order = n_degree + 1;
num_basis_function = b + 1;
num_breaks = num_basis_function - k_order + 2;
breaks = linspace(Global_Min_R, Global_Max_R, num_breaks);
knots = augknt(breaks, k_order);

T = numel(R_observed);
Basis_Precomputed = cell(T, 1);

for t = 1:T
    R_axis_t = Smooth_AllR.(months{t});
    Basis_Precomputed{t} = spcol( ...
        knots, k_order, R_axis_t(:));
end


%% Load compatible observed fits, or estimate them when needed

Observed_Fits_File = fullfile( ...
    Path_Output_18, 'Observed_Model_Fits.mat');

Loaded_Observed_Fits = false;

if Reuse_Observed_Fits && exist(Observed_Fits_File, 'file')
    Saved_Observed = load(Observed_Fits_File);

    if isfield(Saved_Observed, 'Observed_Fits')
        Candidate_Fits = Saved_Observed.Observed_Fits;
        required_models = { ...
            'Constraint', 'Unconstraint', ...
            'Undistorted', 'Distorted'};

        compatible = all(isfield(Candidate_Fits, required_models));

        if compatible
            compatible = ...
                numel(Candidate_Fits.Constraint.theta) == b + 1 && ...
                numel(Candidate_Fits.Unconstraint.theta) == b + 1 && ...
                numel(Candidate_Fits.Undistorted.theta) == b + 1 && ...
                numel(Candidate_Fits.Distorted.theta) == b + 1 && ...
                Candidate_Fits.Constraint.alpha == Shape_Alpha && ...
                Candidate_Fits.Constraint.beta == Shape_Beta && ...
                Candidate_Fits.Unconstraint.alpha == Shape_Alpha && ...
                Candidate_Fits.Unconstraint.beta == Shape_Beta && ...
                Candidate_Fits.Undistorted.alpha == Distortion_Alpha && ...
                Candidate_Fits.Undistorted.beta == Undistorted_Beta && ...
                Candidate_Fits.Distorted.alpha == Distortion_Alpha && ...
                isfield(Candidate_Fits.Distorted, 'beta_grid') && ...
                isequal( ...
                    Candidate_Fits.Distorted.beta_grid(:), ...
                    Distortion_Beta_Grid(:));
        end

        if compatible && ...
                isfield(Saved_Observed, 'Observed_Fit_Settings')
            old_settings = Saved_Observed.Observed_Fit_Settings;

            compatible = ...
                isfield(old_settings, 'Target_TTM') && ...
                isfield(old_settings, 'b') && ...
                isfield(old_settings, 'Sample_Dates') && ...
                old_settings.Target_TTM == Target_TTM && ...
                old_settings.b == b && ...
                isequal( ...
                    old_settings.Sample_Dates(:), ...
                    common_dates(:));
        end

        if compatible
            Observed_Fits = Candidate_Fits;
            Loaded_Observed_Fits = true;
        end
    end
end

if Loaded_Observed_Fits
    fprintf('\nLoading compatible observed-data model fits:\n%s\n', ...
        Observed_Fits_File);

    theta_C = Observed_Fits.Constraint.theta;
    LL_C = Observed_Fits.Constraint.LL;
    exit_C = Observed_Fits.Constraint.exitflag;

    theta_U = Observed_Fits.Unconstraint.theta;
    LL_U = Observed_Fits.Unconstraint.LL;
    exit_U = Observed_Fits.Unconstraint.exitflag;

    theta_N = Observed_Fits.Undistorted.theta;
    LL_N = Observed_Fits.Undistorted.LL;
    exit_N = Observed_Fits.Undistorted.exitflag;

    theta_D = Observed_Fits.Distorted.theta;
    LL_D = Observed_Fits.Distorted.LL;
    exit_D = Observed_Fits.Distorted.exitflag;
    beta_D_hat = Observed_Fits.Distorted.beta;

    fprintf(['Observed MLEs are reused; changing histogram bins ', ...
        'does not require re-estimation.\n']);
    fprintf('Loaded observed beta estimate: %.2f\n', beta_D_hat);

    if ~isfield(Saved_Observed, 'Observed_Fit_Settings')
        Observed_Fit_Settings = struct( ...
            'Target_TTM', Target_TTM, ...
            'b', b, ...
            'Sample_Dates', common_dates(:));
        save(Observed_Fits_File, ...
            'Observed_Fit_Settings', '-append');
    end
else
    fprintf('\nEstimating observed-data models...\n');

    Observed_Timer = tic;
    fprintf('[Observed 1/4] Estimating constrained model...\n');

    [theta_C, LL_C, ~, exit_C] = MLE_BSpline_estimation( ...
        Smooth_AllR, Smooth_AllR_RND, ...
        Realized_Return, Risk_Free_Rate, ...
        b, Shape_Alpha, Shape_Beta, ...
        Global_Min_R, Global_Max_R, ...
        true, Basis_Precomputed);

    fprintf(['[Observed 1/4] Constrained model completed. ', ...
        'LL = %.6f\n'], LL_C);
    fprintf('[Observed 2/4] Estimating unconstrained model...\n');

    [theta_U, LL_U, ~, exit_U] = MLE_BSpline_estimation( ...
        Smooth_AllR, Smooth_AllR_RND, ...
        Realized_Return, Risk_Free_Rate, ...
        b, Shape_Alpha, Shape_Beta, ...
        Global_Min_R, Global_Max_R, ...
        false, Basis_Precomputed);

    fprintf(['[Observed 2/4] Unconstrained model completed. ', ...
        'LL = %.6f\n'], LL_U);
    fprintf('[Observed 3/4] Preparing undistorted model...\n');

    if Shape_Alpha == Distortion_Alpha && ...
            Shape_Beta == Undistorted_Beta
        theta_N = theta_C;
        LL_N = LL_C;
        exit_N = exit_C;
    else
        [theta_N, LL_N, ~, exit_N] = ...
            MLE_BSpline_estimation( ...
            Smooth_AllR, Smooth_AllR_RND, ...
            Realized_Return, Risk_Free_Rate, ...
            b, Distortion_Alpha, Undistorted_Beta, ...
            Global_Min_R, Global_Max_R, ...
            true, Basis_Precomputed);
    end

    fprintf(['[Observed 3/4] Undistorted model completed. ', ...
        'LL = %.6f\n'], LL_N);
    fprintf(['[Observed 4/4] Estimating distorted model ', ...
        'and searching beta...\n']);

    [theta_D, LL_D, ~, exit_D, output_D] = ...
        MLE_BSpline_estimation( ...
        Smooth_AllR, Smooth_AllR_RND, ...
        Realized_Return, Risk_Free_Rate, ...
        b, Distortion_Alpha, Distortion_Beta_Grid, ...
        Global_Min_R, Global_Max_R, ...
        true, Basis_Precomputed);

    beta_D_hat = output_D.SelectedBeta;

    fprintf(['[Observed 4/4] Distorted model completed. ', ...
        'Selected beta = %.2f, LL = %.6f\n'], ...
        beta_D_hat, LL_D);
    fprintf('All observed-data models completed in %.2f minutes.\n', ...
        toc(Observed_Timer) / 60);

    Observed_Fits = struct();
    Observed_Fits.Constraint = struct( ...
        'theta', theta_C, 'LL', LL_C, ...
        'alpha', Shape_Alpha, 'beta', Shape_Beta, ...
        'exitflag', exit_C);
    Observed_Fits.Unconstraint = struct( ...
        'theta', theta_U, 'LL', LL_U, ...
        'alpha', Shape_Alpha, 'beta', Shape_Beta, ...
        'exitflag', exit_U);
    Observed_Fits.Undistorted = struct( ...
        'theta', theta_N, 'LL', LL_N, ...
        'alpha', Distortion_Alpha, ...
        'beta', Undistorted_Beta, ...
        'exitflag', exit_N);
    Observed_Fits.Distorted = struct( ...
        'theta', theta_D, 'LL', LL_D, ...
        'alpha', Distortion_Alpha, ...
        'beta', beta_D_hat, ...
        'beta_grid', Distortion_Beta_Grid, ...
        'grid_log_likelihood', output_D.LogLikelihoodGrid, ...
        'exitflag', exit_D);

    Observed_Fit_Settings = struct( ...
        'Target_TTM', Target_TTM, ...
        'b', b, ...
        'Sample_Dates', common_dates(:));

    save(Observed_Fits_File, ...
        'Observed_Fits', 'Observed_Fit_Settings');
end


